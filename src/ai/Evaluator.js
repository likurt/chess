const PST = {
  p: PAWN_TABLE,
  n: KNIGHT_TABLE,
  b: BISHOP_TABLE,
  r: ROOK_TABLE,
  q: QUEEN_TABLE,
  k: KING_MIDDLE_TABLE,
  k_end: KING_END_TABLE,
};

const PIECE_VALUES = { p: 100, n: 320, b: 330, r: 500, q: 900, k: 20000 };

export function evaluate(chess) {
  if (chess.isCheckmate()) {
    return chess.turn() === 'w' ? -99999 : 99999;
  }
  if (chess.isDraw()) return 0;

  let score = 0;
  const board = chess.board();
  let totalMaterial = 0;

  // 기물 가치 + 위치 보너스
  for (let r = 0; r < 8; r++) {
    for (let c = 0; c < 8; c++) {
      const piece = board[r][c];
      if (!piece) continue;

      const value = PIECE_VALUES[piece.type];
      totalMaterial += (piece.type !== 'k') ? value : 0;

      // PST 인덱스 계산
      let tableIdx;
      if (piece.color === 'w') {
        tableIdx = r * 8 + c;
      } else {
        tableIdx = (7 - r) * 8 + c;
      }

      let pstBonus;
      if (piece.type === 'k') {
        // 엔드게임 판단
        const isEndgame = totalMaterial < 2600;
        pstBonus = isEndgame ? PST.k_end[tableIdx] : PST.k[tableIdx];
      } else {
        pstBonus = PST[piece.type][tableIdx];
      }

      const pieceScore = value + pstBonus;
      score += piece.color === 'w' ? pieceScore : -pieceScore;
    }
  }

  // 모빌리티 보너스 (간단 버전)
  const currentMoves = chess.moves().length;
  const mobilityBonus = currentMoves * 2;
  score += chess.turn() === 'w' ? mobilityBonus : -mobilityBonus;

  return score;
}
